# -----------------------------------------------------

def compute_metrics(eval_pred):

    logits, labels = eval_pred

    preds = np.argmax(
        logits,
        axis=1
    )

    acc = accuracy_score(
        labels,
        preds
    )

    f1 = f1_score(
        labels,
        preds,
        average='weighted'
    )

    return {
        'accuracy': acc,
        'f1': f1
    }

